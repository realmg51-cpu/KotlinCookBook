fun main() {
    // Integer - interger (32 bit)
    val number: Int = 6
    
    // Character - single character (use'')
    val character: Char = 'a'
    
    // String - string (use "")
    val word: String = "I like Kotlin!"
    
    // Float - 32 bit
    val pi: Float = 3.14f
    
    // Double - 64 bit
    val db: Double = 1.677778877
    
    // print it
    println("number: $number")
    println("character: $character")
    println("word: $word")
    println("pi: $pi")
    println("db: $db")
}
